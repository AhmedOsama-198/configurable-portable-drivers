	uint8 i;
	for(i=0;i<7;i++)
	{
		GPIO_setupPinDirection(SEVEN_SEGMENT_PORT_ID, SEVEN_SEGMENT_PIN_ID+i,PIN_OUTPUT);
	}

#elif(DECODER == TRUE)

	GPIO_setupPinDirection(SEVEN_SEGMENT_DECODER_PORT_ID,SEVEN_SEGMENT_DECODER_PIN_ID,PIN_OUTPUT);
	GPIO_setupPinDirection(SEVEN_SEGMENT_DECODER_PORT_ID,SEVEN_SEGMENT_DECODER_PIN_ID+1,PIN_OUTPUT);
