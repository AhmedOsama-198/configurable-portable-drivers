void SEVEN_SEGMENT_enable_First()
{
	GPIO_setupPinDirection(SEVEN_SEGMENT_FIRST_ENABLE_PORT_ID,SEVEN_SEGMENT_FIRST_ENABLE_PIN_ID,PIN_OUTPUT);
	GPIO_writePin(SEVEN_SEGMENT_FIRST_ENABLE_PORT_ID,SEVEN_SEGMENT_FIRST_ENABLE_PIN_ID,LOGIC_HIGH);
}
void SEVEN_SEGMENT_disable_First()
{
	GPIO_writePin(SEVEN_SEGMENT_FIRST_ENABLE_PORT_ID,SEVEN_SEGMENT_FIRST_ENABLE_PIN_ID,LOGIC_LOW);
}
